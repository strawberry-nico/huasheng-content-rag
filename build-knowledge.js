function inferKnowledgeTypes(text) {
  const types = [];
  if (/[0-9]/.test(text) || text.includes("客户") || text.includes("院士") || text.includes("专利")) types.push("fact");
  if (text.includes("痛点") || text.includes("失败") || text.includes("问题") || text.includes("风险")) types.push("pain_point");
  if (text.includes("服务") || text.includes("解决方案") || text.includes("支持") || text.includes("可承接")) types.push("solution");
  if (text.includes("禁用") || text.includes("合规红线") || text.includes("不说") || text.includes("不要")) types.push("compliance_rule");
  if (text.includes("建议使用方向") || text.includes("话术") || text.includes("表达")) types.push("expression_rule");
  return [...new Set(types)];
}

function inferPublicLevel(text) {
  if (text.includes("内部使用") || text.includes("请勿外传")) return "internal";
  if (text.includes("禁用") || text.includes("合规红线") || text.includes("谨慎")) return "restricted";
  return "public";
}

