import fs from "node:fs";
import path from "node:path";

const cwd = process.cwd();
const sourceCandidates = [
  process.env.KNOWLEDGE_SOURCE_FILE,
  "华胜知识库v1.0.md",
  "华胜知识库v1.0 copy.md",
].filter(Boolean);
const sourceFile = sourceCandidates.map((name) => path.join(cwd, name)).find((file) => fs.existsSync(file));

if (!sourceFile) {
  throw new Error("未找到知识库 Markdown 文件");
}

const markdown = fs.readFileSync(sourceFile, "utf8");
const chunks = buildChunks(markdown);
const output = `// Auto-generated by build-knowledge.js
export const KNOWLEDGE_SOURCE = ${JSON.stringify(path.basename(sourceFile), null, 2)};

export const KNOWLEDGE_CHUNKS = ${JSON.stringify(chunks, null, 2)};
`;

fs.writeFileSync(path.join(cwd, "knowledge-data.generated.js"), output, "utf8");
fs.writeFileSync(path.join(cwd, "knowledge-data.generated.json"), JSON.stringify({
  source: path.basename(sourceFile),
  generatedAt: new Date().toISOString(),
  chunks,
}, null, 2), "utf8");
console.log(`Generated ${chunks.length} knowledge chunks from ${path.basename(sourceFile)}`);

function buildChunks(markdownText) {
  const prepared = stripDocumentMeta(markdownText);
  const lines = prepared.replace(/\r\n/g, "\n").split("\n");
  const stack = [];
  const blocks = [];
  let buffer = [];
  let inCodeFence = false;
  let chunkIndex = 0;

  const flushBuffer = () => {
    const text = normalizeBlock(buffer);
    buffer = [];
    if (!text) return;

    const headingPath = stack.map((item) => item.title);
    const title = headingPath[headingPath.length - 1] || "未命名知识";
    if (shouldSkipBlock(headingPath, text)) return;
    const contentParts = splitContent(text, 680);

    for (const content of contentParts) {
      const contextText = `${headingPath.join(" ")} ${content}`;
      blocks.push({
        id: `kb-${String(++chunkIndex).padStart(4, "0")}`,
        title,
        headingPath,
        content,
        topics: inferTopics(contextText),
        businessStages: inferBusinessStages(contextText),
        scenes: inferScenes(contextText),
        knowledgeTypes: inferKnowledgeTypes(contextText),
        publicLevel: inferPublicLevel(contextText),
        keywords: extractKeywords(contextText),
      });
    }
  };

  for (const rawLine of lines) {
    const line = rawLine.trimEnd();

    if (line.startsWith("```")) {
      inCodeFence = !inCodeFence;
      flushBuffer();
      continue;
    }
    if (inCodeFence) continue;
    if (line === "---") {
      flushBuffer();
      continue;
    }

    const headingMatch = line.match(/^(#{1,6})\s+(.+)$/);
    if (headingMatch) {
      flushBuffer();
      const level = headingMatch[1].length;
      const title = cleanText(headingMatch[2]);
      while (stack.length && stack[stack.length - 1].level >= level) stack.pop();
      stack.push({ level, title });
      continue;
    }

    if (!line.trim()) {
      flushBuffer();
      continue;
    }

    buffer.push(line);
  }

  flushBuffer();

  return blocks.filter((item) => item.content.length >= 18);
}

function stripDocumentMeta(markdownText) {
  const normalized = markdownText.replace(/\r\n/g, "\n");
  const firstDivider = normalized.indexOf("\n---\n");
  if (firstDivider === -1) return normalized;
  return normalized.slice(firstDivider + 5);
}

function normalizeBlock(lines) {
  if (!lines.length) return "";
  const joined = lines
    .map((line) => line.trim())
    .filter(Boolean)
    .join("\n");
  return cleanText(joined);
}

function cleanText(text) {
  return text
    .replace(/\*\*/g, "")
    .replace(/`/g, "")
    .replace(/\|/g, " | ")
    .replace(/^[-*]\s+/gm, "")
    .replace(/\s+/g, " ")
    .replace(/\s*\n\s*/g, "\n")
    .trim();
}

function splitContent(text, maxLen) {
  if (text.length <= maxLen) return [text];

  const parts = [];
  const segments = text.split("\n");
  let current = "";

  for (const segment of segments) {
    const next = current ? `${current}\n${segment}` : segment;
    if (next.length <= maxLen) {
      current = next;
      continue;
    }
    if (current) parts.push(current);
    if (segment.length <= maxLen) {
      current = segment;
    } else {
      const sentences = segment.split(/(?<=[。！？；])/);
      let sentenceBuffer = "";
      for (const sentence of sentences) {
        const sentenceNext = sentenceBuffer ? `${sentenceBuffer}${sentence}` : sentence;
        if (sentenceNext.length <= maxLen) {
          sentenceBuffer = sentenceNext;
        } else {
          if (sentenceBuffer) parts.push(sentenceBuffer.trim());
          sentenceBuffer = sentence;
        }
      }
      current = sentenceBuffer.trim();
    }
  }

  if (current) parts.push(current);
  return parts;
}

function shouldSkipBlock(headingPath, text) {
  const pathText = headingPath.join(" > ");
  const syntheticSections = [
    "【0】使用原则",
    "【1】结构化知识标准",
    "【2】华胜业务环节地图",
    "【3】内容与客服共用场景地图",
    "【4】首批结构化知识包样例",
    "【5】优先拆分清单",
    "【6】后续拆分建议",
  ];

  if (syntheticSections.some((title) => pathText.includes(title))) return true;
  if (pathText.includes("【7】原始资料层（保留）")) return true;
  if (/^title:\s|^date:\s|^author:\s|^tags:/i.test(text)) return true;
  if (text.includes("本周立即行动") || text.includes("四周MVP开发计划")) return true;
  if (text.length < 20) return true;
  return false;
}

